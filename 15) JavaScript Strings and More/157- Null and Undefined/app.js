// NULL: Intentional absence of any value. Must be assigned. For example: no one has logged in yet:
let loggedInUser = null; // value has been left intentionally null, it is explicitly nothing.
// when a user logs in:
loggedInUser = "Jack Richman";

// UNDEFINED: Variables that do not have an assigned value are undefined:
"hello"[99]  // undefined
