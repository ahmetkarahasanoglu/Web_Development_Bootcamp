// getElementsByTagName and getElementsByClassName   (attention: they are plural; elements)
// These methods return an HTML Collection. It's an array-like object. It doesn't have array methods; but It has 
// indices and length. 

// Example:
getElementsByTagName("img"); // "img" tag. Just like a, p, div;  img is a tag. we selected all img's.
getElementsByClassName("square"); // images that have "square" class.
